/*
 单词缩写规则
 Object     -> Obj
 String     -> Str
 Array      -> Arr
 Dictionary -> Dic
 Parameters -> Param
 
 指定唯一标识符命名 Identifier
 
 private修饰的扩展参数需以_开头，无需前缀
 */


/*
 操作符参数命名规则
 左参数 leftValue
 右参数 rightValue
 中参数 centerValue
 驼峰式、首字母小写、不带前缀、根据方位+Value模式作为命名规范
 */

/*
 mutating命名规则
 能修改自身的函数方法，不得有命名歧义的其他方法
 修改自身函数方法必须唯一
 */

/*
 类方法函数命名规则
 axc_后的首字母要大写，采用驼峰式
 */

/*
 全局方法参数命名规则
 全局方法命名，Axc+方法名，驼峰式
 全局参数命名，Axc_+参数名，驼峰式
 */

/*
 struct结构体命名规则
 结构体本身开头大写，驼峰式
 结构体内部参数无需axc前缀
 参数方法均小写开头，驼峰式
 */

