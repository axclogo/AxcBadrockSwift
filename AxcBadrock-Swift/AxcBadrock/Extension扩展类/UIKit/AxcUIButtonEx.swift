// MARK: - 类方法/属性
public extension UIButton {
    /// 初始化
    /// - Parameters:
    ///   - title: 标题
    ///   - image: 图片
    convenience init(title: String? = nil, image: UIImage? = nil) {
        self.init()
        setTitle(title, for: .normal)
        setImage(image, for: .normal)
    }
}

